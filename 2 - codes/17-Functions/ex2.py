
#whatever written inside of function def () is known as parameter
def sum(num1, num2):
    print(num1+num2)

def sub(num1, num2):
    print(num1-num2)

def mul(num1, num2):
    print(num1*num2)

def divi(num1, num2):
    print(num1/num2)

num1 = int(input("enter num1 : "))
num2 = int(input("enter num2 : "))

sum(num1, num2)  #arguments
sub(num1, num2)
mul(num1, num2)
divi(num1, num2)